package org.opentutorials.javatutorials.constant;

public class ConstantDemo {

	public static void main(String[] args) {
		float a = 2.2;
		float a = 2.2F; //F 가 속한 숫자가  float 데이터 타입이란걸 명시적으로 표현
	    long a = 2147483648;
	    long a = 2147483648L;
	    byte b = 100; // 기본적으로 int형 대신 범위안에 있어야됨
	}

}
